/* Relation operator consist greater than (>), Less than(<), Equal to(==) and Not equal to(!=),
greater than equal to (=>), Less than equal to(<=)  operators are used in Javascript.*/

//relation operator are done on operand 
let a=10;
let b=5;

console.log(a>b)   //Greater than
console.log(a<b)  //less than
console.log(a>= b) // greater than equal to
console.log(a<=b)  //less than equal to
console.log(a==b)  // equal to
console.log(a!=b) // not equal to